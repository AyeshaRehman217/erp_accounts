    public RouterFunction<ServerResponse> VoucherGroupRoutes(VoucherGroupHandler handle) {
        return RouterFunctions.route(GET("account/api/v1/voucher-groups/index").and(accept(APPLICATION_FORM_URLENCODED)), handle::index).filter(new IndexVoucherGroupHandlerFilter())
                .and(RouterFunctions.route(GET("account/api/v1/voucher-groups/active/index").and(accept(APPLICATION_FORM_URLENCODED)), handle::indexWithActiveStatus).filter(new IndexVoucherGroupHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/voucher-groups/show/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::show).filter(new ShowVoucherGroupHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/voucher-groups/voucher/mapped/show/{voucherUUID}").and(accept(APPLICATION_FORM_URLENCODED)), handle::showVoucherWithVoucherGroups).filter(new IndexVoucherGroupAgainstVoucherHandlerFilter()))
                .and(RouterFunctions.route(DELETE("account/api/v1/voucher-groups/delete/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::delete).filter(new ShowVoucherGroupHandlerFilter()))
                .and(RouterFunctions.route(POST("account/api/v1/voucher-groups/store").and(accept(APPLICATION_FORM_URLENCODED)), handle::store).filter(new StoreVoucherGroupHandlerFilter()))
                .and(RouterFunctions.route(PUT("account/api/v1/voucher-groups/update/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::update).filter(new UpdateVoucherGroupHandlerFilter()))
                .and(RouterFunctions.route(PUT("account/api/v1/voucher-groups/status/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::status).filter(new ShowVoucherGroupHandlerFilter()));
    }

