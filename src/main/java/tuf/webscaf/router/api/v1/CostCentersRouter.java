    public RouterFunction<ServerResponse> CostCenterRoutes(CostCenterHandler handle) {
        return RouterFunctions.route(GET("account/api/v1/cost-centers/index").and(accept(APPLICATION_FORM_URLENCODED)), handle::index).filter(new IndexCostCenterHandlerFilter())
                .and(RouterFunctions.route(GET("account/api/v1/cost-centers/active/index").and(accept(APPLICATION_FORM_URLENCODED)), handle::indexWithActiveStatus).filter(new IndexCostCenterHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/cost-centers/branch/show/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::checkIfBranchExists).filter(new ShowCostCenterHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/cost-centers/company/show/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::checkIfCompanyExists).filter(new ShowCostCenterHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/cost-centers/show/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::show).filter(new ShowCostCenterHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/cost-centers/voucher/company/show/{voucherUUID}").and(accept(APPLICATION_FORM_URLENCODED)), handle::showCostCentersWithCompany).filter(new ShowCostCenterWithVoucherAndCompanyHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/cost-centers/voucher/branch/show/{voucherUUID}").and(accept(APPLICATION_FORM_URLENCODED)), handle::showCostCentersWithBranch).filter(new ShowCostCenterWithVoucherAndBranchHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/cost-centers/voucher/show/{voucherUUID}").and(accept(APPLICATION_FORM_URLENCODED)), handle::showVoucherWithCostCenters).filter(new ShowCostCenterWithVoucherHandlerFilter()))
                .and(RouterFunctions.route(POST("account/api/v1/cost-centers/store").and(accept(APPLICATION_FORM_URLENCODED)), handle::store).filter(new StoreCostCenterHandlerFilter()))
                .and(RouterFunctions.route(PUT("account/api/v1/cost-centers/update/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::update).filter(new UpdateCostCenterHandlerFilter()))
                .and(RouterFunctions.route(PUT("account/api/v1/cost-centers/status/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::status).filter(new ShowCostCenterHandlerFilter()))
                .and(RouterFunctions.route(DELETE("account/api/v1/cost-centers/delete/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::delete).filter(new DeleteCostCenterHandlerFilter()));
    }
