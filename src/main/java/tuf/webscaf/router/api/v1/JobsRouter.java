    public RouterFunction<ServerResponse> JobRoutes(JobHandler handle) {
        return RouterFunctions.route(GET("account/api/v1/jobs/index").and(accept(APPLICATION_FORM_URLENCODED)), handle::index).filter(new IndexJobHandlerFilter())
                .and(RouterFunctions.route(GET("account/api/v1/jobs/active/index").and(accept(APPLICATION_FORM_URLENCODED)), handle::indexWithActiveStatus).filter(new IndexJobHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/jobs/branch/show/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::getBranchUUID).filter(new ShowJobHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/jobs/company/show/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::getCompanyUUID).filter(new ShowJobHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/jobs/show/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::show).filter(new ShowJobHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/jobs/voucher/show/{voucherUUID}").and(accept(APPLICATION_FORM_URLENCODED)), handle::showVoucherWithJobs).filter(new ShowJobWithVoucherHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/jobs/voucher/company/show/{voucherUUID}").and(accept(APPLICATION_FORM_URLENCODED)), handle::showJobsWithCompany).filter(new ShowWithVoucherAndCompanyHandlerFilter()))
                .and(RouterFunctions.route(GET("account/api/v1/jobs/voucher/branch/show/{voucherUUID}").and(accept(APPLICATION_FORM_URLENCODED)), handle::showJobsWithBranch).filter(new ShowWithVoucherAndBranchHandlerFilter()))
                .and(RouterFunctions.route(DELETE("account/api/v1/jobs/delete/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::delete).filter(new ShowJobHandlerFilter()))
                .and(RouterFunctions.route(POST("account/api/v1/jobs/store").and(accept(APPLICATION_FORM_URLENCODED)), handle::store).filter(new StoreJobHandlerFilter()))
                .and(RouterFunctions.route(PUT("account/api/v1/jobs/update/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::update).filter(new UpdateJobHandlerFilter()))
                .and(RouterFunctions.route(PUT("account/api/v1/jobs/status/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::status).filter(new ShowJobHandlerFilter()));
    }

